package class02;

public class DecimalNumber {
    public static void main(String[] args) {
        /*
        if we want to store decimal numbers in the computer's memory using java, we can use following types
        float
        double
         */
        float myNumberBox=10.5f;
        float mySecondBox=5.5f;
        double largeDecimalBox=12344556.12345;
        System.out.println(myNumberBox);

        /*
        If we want to store characters(#,@,$,%,5) we can use char datatype
        */

        char letter='-';
        char gender='M';
        char symbol='@';
        /*
        String data type belongs to a different category called non-primitive, that's why for String rules, there are
        differences, for example S of String is upper case its color is also not blue
         */


        String name="Ashley";
        boolean hungry=true; //true means yes
        boolean sleepy=false; //false means no



    }
}
